void primes(int n)
{
    vi prime(n + 1, 1); // intially all true
    for (int i = 2; i * i <= n; i++)
    {
        if (prime[i] == 1)
        { // multiples mark kardo
            for (int j = i * i; j <= n; j += i)
            {
                prime[j] = 0;
            }
        }
    }
}

